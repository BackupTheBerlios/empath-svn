import unittest, re
from smanstal.tests import BaseUnitTest, addtest, mksuite

from eqobj.validators.user import UserDefined

class Test_compare(BaseUnitTest): #{{{
    def setUp(self): #{{{
        pass
    # End def #}}}

    def tearDown(self): #{{{
        pass
    # End def #}}}

    def test_compare(self): #{{{
        '''int isinstance compare function'''
        a = UserDefined(lambda o: isinstance(o, int))
        for i in xrange(10):
            self.assertTrue(a(i))
        for o in ['a', 1.5, UserDefined, a]:
            self.assertFalse(a(o))
    # End def #}}}
# End class #}}}

# Create suite function for this module
suite = addtest(mksuite(__file__))

if __name__ == '__main__':
    unittest.TextTestRunner(verbosity=2).run(suite())

